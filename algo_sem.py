def selection_sort(arr): # spacial O(1), time O(n^2)
    n = len(arr)
    for i in range(n - 1):
        min = arr[i]
        min_i = i
        for j in range(i+1, n):
            if arr[j] < min:
                min_i = j
                min = arr[j]

        temp = arr[i]
        arr[i] = arr[min_i]
        arr[min_i] = temp
        #print(arr)

