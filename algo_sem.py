import numpy as np
from timeit import default_timer as timer
import matplotlib.pyplot as plt

def random_sample(n):
    return [int(i) for i in np.random.randint(n, size=n)]

def reversed_sample(n):
    return list(range(n)[::-1])

def almost_sorted_sample(n):
    sample = list(range(1, n + 1))
    sample[n//2] = 0
    return sample

def half_zero_sample(n):
    return [0]*(n//2) + random_sample(n - n//2)

def slightly_scuffed_sample(n):
    return [int(i) for i in (np.arange(n) + np.random.randint(10, size=n))]

def test(sorting_function):
    ns = [100, 500, 1000, 2000, 4000, 6000, 8000, 10000]
    sample_types = [random_sample, reversed_sample, almost_sorted_sample, half_zero_sample, slightly_scuffed_sample]
    for sample_function in sample_types:
        ts = []
        for n in ns:
            sample1 = sample_function(n)
            sample2 = sample_function(n)
            sample3 = sample_function(n)
            start = timer()
            sorting_function(sample1)
            sorting_function(sample2)
            sorting_function(sample3)
            stop = timer()
            ts.append((stop - start)/3)

        plt.figure(figsize=(3, 3))
        plt.plot(ns, ts)
        plt.title(sample_function.__name__)
        plt.show()

test(selection_sort)

